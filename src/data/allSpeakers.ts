// Add poster dimensions and multi-discipline support to all speakers
allSpeakers.forEach((speaker, index) => {
  // Add poster dimensions if missing
  if (!speaker.posterWidth || !speaker.posterHeight) {
    const size = getRandomPosterSize(index);
    speaker.posterWidth = size.width;
    speaker.posterHeight = size.height;
    speaker.posterUrl = getPosterImageByAspectRatio(size.width, size.height, index);
  }
  
  // Add disciplines array from the discipline map
  const mappedDisciplines = getDisciplinesForSpeaker(speaker.name);
  if (mappedDisciplines) {
    speaker.disciplines = mappedDisciplines;
    // Update primary discipline to first in array
    speaker.discipline = mappedDisciplines[0];
  } else if (!speaker.disciplines) {
    // Fallback: use existing discipline as single-item array
    speaker.disciplines = [speaker.discipline];
  }
});

// Helper: Speakers that need biographical enrichment
export const speakersNeedingEnrichment = allSpeakers.filter(
  speaker => speaker.bio.includes("More information to be added")
);

// Helper: Count by discipline
export const speakerCountByDiscipline = allSpeakers.reduce((acc, speaker) => {
  acc[speaker.discipline] = (acc[speaker.discipline] || 0) + 1;
  return acc;
}, {} as Record<string, number>);
