/**
 * Get disciplines for a speaker by name lookup
 * Returns an array of disciplines or undefined if not found
 */
export function getDisciplinesForSpeaker(name: string): string[] | undefined {
  // Direct lookup
  if (speakerDisciplineMap[name]) {
    return speakerDisciplineMap[name];
  }
  
  // Try normalized name (trimmed, consistent spacing)
  const normalizedName = name.trim().replace(/\s+/g, ' ');
  if (speakerDisciplineMap[normalizedName]) {
    return speakerDisciplineMap[normalizedName];
  }
  
  return undefined;
}

